    private void setAcc(int id) {
        AccountControl accc = new AccountControl();
        this.acc = accc.getAccountById(id);
        this.prepareTable();
    }

    private void prepareTable() {
        this.boxes = BoxControl.listBoxByIdAcc(this.acc.getId());

        this.clearTable();
        this.populateTable();
    }

    private void clearTable() {
        while (TBLBoxes.getRowCount() > 0) {
            DefaultTableModel dm = (DefaultTableModel) TBLBoxes.getModel();
            dm.getDataVector().removeAllElements();
        }
    }

    private void populateTable() {
        this.dfltBoxes = (DefaultTableModel) this.TBLBoxes.getModel();
        for (Box box : boxes) {
            dfltBoxes.addRow(new Object[]{box.getId(), box.getName(), box.getDescription()});
        }
        TBLBoxes.setModel(dfltBoxes);
    }

    private void setDefaultRender() {
        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment(JLabel.CENTER);
        TBLBoxes.setDefaultRenderer(String.class, centerRenderer);
    }

    private void showDocumets() {
        String txt = "";
        for (Document doc : this.boxClicked.getDocuments()) {
            txt+=doc.getName()+" \n";
        }
    }
