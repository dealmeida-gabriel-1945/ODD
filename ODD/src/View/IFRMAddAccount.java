    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        JFileChooser f = new JFileChooser();
        f.setDialogTitle("Selecionar foto");
        f.setFileFilter(new FileNameExtensionFilter("Image files", "bmp", "png", "jpg"));
        f.setAcceptAllFileFilterUsed(false);
        f.setCurrentDirectory(new File(System.getProperty("user.home") + "/Desktop"));
        f.setFileSelectionMode(JFileChooser.FILES_ONLY);
        int res = f.showOpenDialog(null);
        if (res == JFileChooser.APPROVE_OPTION) {
            photoFile = f.getSelectedFile();
            try {
                LBLPhoto.setText("");
                ImageIcon imageIcon = new ImageIcon(photoFile.getPath()); // load the image to a imageIcon
                this.photoPath = photoFile.getPath();
                Image image = imageIcon.getImage(); // transform it
                Image newimg = image.getScaledInstance(120, 120, java.awt.Image.SCALE_SMOOTH); // scale it the smooth way
                image = newimg;
                LBLPhoto.setIcon(new ImageIcon(image));

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error while selecting file");
            }
        } else if (res == JFileChooser.CANCEL_OPTION) {
        } else {
            JOptionPane.showMessageDialog(null, "Error");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

