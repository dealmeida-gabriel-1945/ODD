    private void setDefaultRender() {
        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment(JLabel.CENTER);
        TBLBoxes.setDefaultRenderer(String.class, centerRenderer);
    }

    private void setAcc(int id) {
        this.acc = AccountControl.getAccountById(id);
        this.populateTable();
    }

    private boolean checkFields() {
        if (TFName.getText().equals("") || TFType.getText().equals("")) {
            return true;
        }else{
            return false;
        }
    }

    private void populateTable() {
        this.boxes = BoxControl.listBoxByIdAcc(this.acc.getId());
        this.deleteAllRows();
        this.populate();
    }

    private void deleteAllRows() {
        while (TBLBoxes.getRowCount() > 0) {
            DefaultTableModel dm = (DefaultTableModel) TBLBoxes.getModel();
            dm.getDataVector().removeAllElements();
        }
    }

    private void populate() {
        dtm = (DefaultTableModel) this.TBLBoxes.getModel();
        for (Box box : boxes) {
            
            dtm.addRow(new Object[]{Boolean.FALSE, box.getId(), box.getName(), box.getDescription()});
        }
        TBLBoxes.setModel(dtm);
    }

    private void setAllFalse() {
        for (int i = 0; i < TBLBoxes.getRowCount(); i++) {
            if (TBLBoxes.getValueAt(i, 0).equals(Boolean.TRUE) && (i == index)) {

            } else {
                TBLBoxes.setValueAt(Boolean.FALSE, i, 0);
            }
        }
    }

    private Document buildDocument() {
        Document doc = new Document();
        doc.setName(TFName.getText());
        doc.setType(TFType.getText());
        doc.setAccount_id(this.acc.getId());
        for (int i = 0; i < TBLBoxes.getRowCount(); i++) {
            if (TBLBoxes.getValueAt(i, 0).equals(Boolean.TRUE)) {
                doc.setBox_id(boxes.get(i).getId());
            }
        }
        return doc;
    }

    private void register(Document doc) {
        System.out.println(doc.getName());
        if (DocumentControl.addDocument(doc)) {
            Warnings.wrngAddDocumentFail();
        } else {
            Warnings.wrngAddDocumentSucces();
            
            IFRMAddDocument iadddoc = new IFRMAddDocument(this.acc.getId());
            javax.swing.plaf.InternalFrameUI ifu = iadddoc.getUI();
            ((javax.swing.plaf.basic.BasicInternalFrameUI) ifu).setNorthPane(null);
            this.getParent().add(iadddoc);
            try {
                iadddoc.setMaximum(true);
            } catch (Exception e) {
                e.printStackTrace();
            }
            iadddoc.show();
            this.dispose();
        }
    }
